#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	v;

	i = 0;
	v = 1;
	while (str[i] != '\0' && v == 1)
	{
		if (str[i] >= 65 && str[i] <= 90)
			v = 1;
		else
			v = 0;
		i++;
	}
	return (v);
}
