int	ft_find_next_prime(int nb)
{
	int	i;
	int	aux;

	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
			aux = 0;
		i++;
	}
	if (aux == 0)
	{
		nb++;
		return (ft_find_next_prime(nb));
	}
	return (nb);
}
