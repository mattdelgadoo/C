#include <stdio.h>
#include <unistd.h>

int		g_a;
int		g_b;
char	g_c[7];

void	ft_print_comb2_1(void)
{
	g_c[0] = g_a / 10 + '0';
	g_c[1] = g_a % 10 + '0';
	g_c[2] = ' ';
	g_c[3] = g_b / 10 + '0';
	g_c[4] = g_b % 10 + '0';
	g_c[5] = ',';
	g_c[6] = ' ';
	if (g_a == 99)
		write(1, &g_c, 5);
	else
		write(1, &g_c, 7);
}

void	ft_print_comb2(void)
{
	g_a = 0;
	g_b = 1;
	while (g_b <= 99 && g_a <= 99)
	{
		ft_print_comb2_1();
		g_b++;
		g_c[3] = g_b / 10 + '0';
		g_c[4] = g_b % 10 + '0';
		while (g_b == 100)
		{
			g_b = g_a + 2;
			g_c[0] = g_a / 10 + '0';
			g_c[1] = g_a % 10 + '0';
			g_a++;
		}
	}
}				
